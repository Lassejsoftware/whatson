


public class Beer {

String beer;
String brewery;
String url;


public Beer(String beer, String brewery, String url){

    this.beer = beer;
    this.brewery = brewery;
    this.url = url;


}

    public String getBeer() {
        return beer;
    }

    public String getBrewery() {
        return brewery;
    }

    public String getUrl() {
        return url;
    }
}
